     */
    public function show($id)
    {
        $user=User::find($id);

        $products=Cart::where('user_id', '=', $user->id)->where('status', '=', 1)->groupBy('product_id')->selectRaw('*, SUM(cant) as cantidad, price*SUM(cant) as precio')->orderByRaw('cantidad DESC')->get();


        return view('userdetail', compact('user', 'products'));
    }

